    public static void run(char[][] board, String word, boolean correctAnswer) {
        Utils.print("start app with: ", board);
        boolean result = new Solution().exist(board, word);
        Assert.equals(correctAnswer, result);
    }

    static class Solution {
        private boolean backtracking(char[][] board, String word, int posInWord, int i, int j) {
            if (posInWord == word.length()) return true;

            if (i < 0 || j < 0 || i >= board.length || j >= board[0].length) return false;
            if (board[i][j] != word.charAt(posInWord)) return false;

            char c = board[i][j];
            board[i][j] = '#';

            boolean res =
                    backtracking(board, word, posInWord + 1, i + 1, j) ||
                            backtracking(board, word, posInWord + 1, i, j + 1) ||
                            backtracking(board, word, posInWord + 1, i - 1, j) ||
                            backtracking(board, word, posInWord + 1, i, j - 1);

            board[i][j] = c;
            return res;
        }

        public boolean exist(char[][] board, String word) {


            for (int i = 0; i < board.length; i++) {
                for (int j = 0; j < board[0].length; j++) {
                    if (board[i][j] == word.charAt(0)) {
                        boolean res = backtracking(board, word, 0, i, j);
                        if (res) {
                            return true;
                        }
                    }
                }
            }

            return false;
        }
    }
